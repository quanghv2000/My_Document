/*
    Các loại function

        1. Declaration function
        2. Expression function
        3. Arrow function
*/

// 1. Declaration function
function declareFunc() {
    console.log("Declaration function!");
}

// 2. Expression function
var expressFunc = function () {
    console.log("Expression function!")
}

// 3. Arrow function
const arrowFunc = () => {
    console.log("Arrow function!")
}